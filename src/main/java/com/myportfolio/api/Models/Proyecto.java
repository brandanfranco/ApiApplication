package com.myportfolio.api.Models;

import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;

import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 *
 * @author brand
 */
@Entity
@Table(name = "proyectos")
public class Proyecto {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    private String nombre;

    @Column(name = "url_project", length = 2048)
    private String url;

    @Column(name = "tecnologias_usadas", length = 2048)
    private String tecnologias;

    @Column(name = "descripcion", length = 2048)
    private String descripcion;
    
   
    

    public Proyecto() {
    }

    

    public Proyecto(Long id, String nombre, String url, String tecnologias, String descripcion) {
        this.id = id;
        this.nombre = nombre;
        this.url = url;
        this.tecnologias = tecnologias;
        this.descripcion = descripcion;
        
    }

    

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTecnologias() {
        return tecnologias;
    }

    public void setTecnologias(String tecnologias) {
        this.tecnologias = tecnologias;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

   

    
}
